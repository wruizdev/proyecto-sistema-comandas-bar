    private void SetDate() {
        LocalDate now = LocalDate.now();
        int anio = now.getYear();
        int dia = now.getDayOfMonth();
        int mes = now.getMonthValue();
        String[] meses = {"Enero",
            "Febrero",
            "Marzo",
            "Abril",
            "Mayo",
            "Junio",
            "Julio",
            "Agosto",
            "Septiembre",
            "Octubre",
            "Noviembre",
            "Diciembre"};

        dateText.setText(dia + " de " + meses[mes - 1] + " de " + anio);
    }
