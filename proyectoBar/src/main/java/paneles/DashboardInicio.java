    private void ShowJPanel(JPanel pr) {
        // Verifica si el content ya tiene algún componente
        if (content.getComponentCount() > 0) {
            // Si es así, elimina todos los componentes existentes
            content.removeAll();
        }
 
        int width = java.awt.Toolkit.getDefaultToolkit().getScreenSize().width;
        int height = java.awt.Toolkit.getDefaultToolkit().getScreenSize().height;
        pr.setSize(width - 270, height - 76);
        pr.setLocation(0, 0);
 
        content.add(pr, BorderLayout.CENTER);
        content.revalidate();
        content.repaint();
 
    }
