    public boolean IsSelected2(int fila, int columna, JTable tabla) {
    // Verificamos que la fila y la columna estén dentro de los límites válidos
    if (fila >= 0 && fila < tabla.getRowCount() && columna >= 0 && columna < tabla.getColumnCount()) {
        // Comprobamos si la celda en la posición especificada está seleccionada
        return tabla.getValueAt(fila, columna)!= null;
    } else {
        // Devolvemos false si la fila o la columna están fuera de los límites
        return false;
    }
}
    
 public void addEditableTextColumn(int columna, JTable tabla) {
    TableColumn tableColumn = tabla.getColumnModel().getColumn(columna);
    tableColumn.setCellEditor(new DefaultCellEditor(new JTextField()));
    tableColumn.setCellRenderer(new DefaultTableCellRenderer());
    
}
