     //Método para añadir checkbox en cada columna
    public void addCheckBox(int columna, JTable tabla) {
        TableColumn tablec = tabla.getColumnModel().getColumn(columna);
        tablec.setCellEditor(tabla.getDefaultEditor(Boolean.class));
        tablec.setCellRenderer(tabla.getDefaultRenderer(Boolean.class));
    }

