    private void btnAñadirPlatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirPlatosActionPerformed
        for (int i = 0; i < tablaPlatos.getRowCount(); i++) {
        String codigoPlato = "";
        int cantidadPlato = 0;
        String codigoComanda = txtcodigoComanda.getText();

        // Verificamos si la fila está seleccionada y si la columna cantidad tiene un valor
        if (IsSelected2(i, 2, tablaPlatos)) {
            codigoPlato = tablaPlatos.getValueAt(i, 0).toString();
            // Intentamos obtener el valor de la columna cantidad y convertirlo a int
            String cantidadStr = tablaPlatos.getValueAt(i, 3).toString();
            if (!cantidadStr.isEmpty()) {
                try {
                    cantidadPlato = Integer.parseInt(cantidadStr);
                } catch (NumberFormatException e) {
                    // Manejamos el caso en que el valor no es un número válido
                    System.err.println("Error al convertir la cantidad a int: " + e.getMessage());
                    continue; // Saltamos el resto del bucle para esta iteración
                }
            }
        }

        // Solo intentamos insertar si se tienen datos válidos
        if (!codigoPlato.isEmpty() &&!codigoComanda.isEmpty() && cantidadPlato > 0) {
            crud.insertarPlatosComanda(codigoPlato, codigoComanda, cantidadPlato);
        }
    }
    }//GEN-LAST:event_btnAñadirPlatosActionPerformed

